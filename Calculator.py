def add(n1, n2):
    return n1 + n2

def subtract(n1, n2):
    return n1 - n2

def multiply(n1, n2):
    return n1 * n2

def divide(n1, n2):
    return n1 / n2


operators = {
    "+": add,
    "-": subtract,
    "*": multiply,
    "/": divide
}


def calculate():
    print(logo)
    n1 = float(input("Enter the first number: "))
    stop = False
    while not stop:
        for op in operators:
            print(op)

        op = input("Choose your operator: ")
        n2 = float(input("Enter the second number: "))

        result = operators[op](n1, n2)
        print(f"{n1} {op} {n2} = {result}")

        choice = input("Would you like to continue the calculation? (Y/N): ").upper()
        if choice == "Y":
            n1 = result
        else:
            stop = True
            print(10 * "\n")
            calculate()
            return


calculate()
